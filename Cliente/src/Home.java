                model.addRow(new Object[]{String.valueOf(pos), tab.getCasillas(pos), "0", "0",
                String.valueOf(alquiler), String.valueOf(venta)});
                
               
                
                _out.writeInt(15); // Mensaje enviado solicitando saldo actual
                mensaje = _input.readInt();
